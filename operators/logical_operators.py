#logical operators
# and

print(1<2 and 2>1 and 2<3)
print (2>3 and 2>1 and 2<3)

#True and True = True
#True and False = False
#False and True = False
#False and False = False

#or
print(2<5 or 3<2)
print(2>3 or 5>8)

# False or False = False
# True or False = True
# False or True = True
# True or True = True

#not

print(not(2>3 or 1>2))
print(not(5<10 and 10>2))

#not True = False
#not False = True
